    private void txtDiscountKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDiscountKeyReleased
        boolean empty = txtDiscount.getText().isEmpty();
        if (empty) {
            calclulateTotal();
        } else {

            double disc = Double.parseDouble(txtDiscount.getText());
            if (disc > 0) {
                DefaultTableModel model = (DefaultTableModel) tblTbl.getModel();

                double tempTotal = 0;
                double discount = Double.parseDouble(txtDiscount.getText());

                for (int i = 0; i < model.getRowCount(); i++) {
                    tempTotal += (Double) model.getValueAt(i, 7);
                }

                lblFinelTotal.setText("Total : " + (tempTotal - (tempTotal / 100 * discount)) + "/=");
            }
        }


    }//GEN-LAST:event_txtDiscountKeyReleased

    private void txtDiscount1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDiscount1KeyReleased

        double discountTotal = Integer.parseInt(txtQty.getText()) * Double.parseDouble(txtUnitPrice.getText());
        boolean empty = txtDiscount1.getText().isEmpty();
        int discount = Integer.parseInt(txtDiscount1.getText());
        if (empty) {
            txtQtyKeyReleased(evt);
        } else {
            txttotal1.setText((discountTotal - (discountTotal / 100 * discount)) + "");
        }
    }//GEN-LAST:event_txtDiscount1KeyReleased

