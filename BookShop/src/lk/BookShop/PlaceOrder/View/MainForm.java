    private void setOrderId() throws ClassNotFoundException, SQLException {
        String orderId = new MainFormController().getOrderId();

        if (orderId != null) {
            String[] temparr = orderId.split("OR");
            int tempNumber = Integer.parseInt(temparr[1]);
            tempNumber += 1;

            if (tempNumber < 10) {
                txtOrderId.setText("OR00" + tempNumber);
            } else {
                txtOrderId.setText("OR0" + tempNumber);
            }

        } else {
            txtOrderId.setText("O001");
        }

    }

