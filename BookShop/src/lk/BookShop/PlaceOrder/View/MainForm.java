    private int isAlreadyExists(String code) {
        DefaultTableModel model = (DefaultTableModel) tblTbl.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            String temp = model.getValueAt(i, 2).toString();
            if (temp.equalsIgnoreCase(code)) {
                return i;
            }

        }
        return -1;
    }

    private void calclulateTotal() {
        DefaultTableModel model = (DefaultTableModel) tblTbl.getModel();

        double tempTotal = 0;

        for (int i = 0; i < model.getRowCount(); i++) {
            tempTotal += (Double) model.getValueAt(i, 7);
        }

        lblFinelTotal.setText(tempTotal + "");

    }

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblTbl.getModel();

        int Qty = Integer.parseInt(txtQty.getText());
        Double UnitPrice = Double.parseDouble(txtUnitPrice.getText());
        Double Total = Qty * UnitPrice;
        int discount = Integer.parseInt(txtDiscount1.getText());

        int rowIndex = isAlreadyExists(txtItemid.getSelectedItem().toString());

        if (rowIndex == -1) {
            Object[] rowData = {
                txtOrderId.getText(),
                txtCustid.getSelectedItem().toString(),
                txtItemid.getSelectedItem().toString(),
                txtItem.getText(),
                Qty,
                UnitPrice,
                discount,
                Total
            };
            model.addRow(rowData);

        } else {
            Qty += (int) model.getValueAt(rowIndex, 4);
            Total = Qty * UnitPrice;
            tblTbl.setValueAt(Qty, rowIndex, 4);
            tblTbl.setValueAt(Total, rowIndex, 6);

        }
        calclulateTotal();

    }//GEN-LAST:event_btnAddActionPerformed

